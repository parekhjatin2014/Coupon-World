package com.example.app.model;

public class Status {

    private int rcode;
    private String message;

    public int getRcode() {
        return this.rcode;
    }

    public void setRcode(int rcode) {
        this.rcode = rcode;
    }

    public String getMessage() {
        return this.message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}

